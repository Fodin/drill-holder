"""Калибровочный купон посадки под ваш принтер/пластик — печатается один раз.

ЗАЧЕМ. FDM печатает отверстия меньше номинала (усадка + раздув бисера на тугих радиусах),
и величина у каждого принтера/пластика/потока своя. Этот купон даёт измерить ИМЕННО ВАШ
зазор свободной посадки за одну печать, а не угадывать.

КАК ЭТО РАБОТАЕТ. Зазор `socket.clearance` — глобальный, в одной печати его не поменять по
гнёздам. Но диаметр бора = d + 2·clearance, поэтому «разный зазор для одного реального сверла»
кодируется раздутым d при clearance = 0. Пружины выключены — калибруем чистую посадку бора.
Подпись гнезда: «<сверло>·<зазор×100>», напр. «3·20» = сверло 3 мм, зазор 0.20 мм на радиус.
Вся логика — в drillholder.config.calibration (один источник для CLI и GUI-кнопки «Калибровка»).

ЧТО ДЕЛАТЬ.
  1. ./drill-holder --no-gui calibration.py   (печать STL; ~10–15 мин, мало пластика)
  2. Вставьте РЕАЛЬНОЕ сверло каждого размера в его ряд гнёзд.
  3. Найдите минимальный зазор, при котором сверло входит свободно (без клина), но не болтается.
  4. Впишите это число в socket.clearance вашего holder_config.py.
     Если мелкие и крупные просят разное — берите по мелким (их держат детенты, лёгкий люфт
     крупных не страшен), либо печатайте мелкие отдельным конфигом с большим зазором.

Подбирайте размеры/зазоры под свой набор: правьте TEST_DRILLS и CLEARANCES ниже.
"""

import os
import sys

# Чтобы `import drillholder` работал и при загрузке этого файла напрямую (importlib/freecadcmd).
_HERE = os.path.dirname(os.path.abspath(__file__))
if _HERE not in sys.path:
    sys.path.insert(0, _HERE)

from drillholder.config import calibration_config

# Реальные диаметры свёрел для проверки, мм (проблемный мелкий край + один средний).
TEST_DRILLS = [2.0, 3.0, 4.0]
# Радиальные зазоры-кандидаты, мм. 0.20 — типичный FDM-дефолт; шире/уже — разброс под усадку.
CLEARANCES = [0.10, 0.15, 0.20, 0.25, 0.30, 0.35]

CONFIG = calibration_config(TEST_DRILLS, CLEARANCES)
