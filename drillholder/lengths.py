"""Справочник стандартных длин свёрел (DIN 338, тип N — jobber) — чистый модуль без FreeCAD.

Когда длина сверла не задана в конфиге (или указана как ``"auto"``), общая длина берётся
отсюда по ближайшему сверху стандартному диаметру. Значения — номинальная полная длина L
по DIN 338 (тип N), мм. Используется и ядром (config), и GUI (подстановка в таблицу).
"""

from __future__ import annotations

from typing import Dict

# Диаметр (мм) → полная длина L по DIN 338 (тип N). Покрывает ходовой ряд 1–20 мм.
DIN338_LENGTHS: Dict[float, float] = {
    1.0: 34.0, 1.5: 40.0, 2.0: 49.0, 2.5: 57.0, 3.0: 61.0, 3.5: 70.0,
    4.0: 75.0, 4.5: 80.0, 5.0: 86.0, 5.5: 93.0, 6.0: 93.0, 6.5: 101.0,
    7.0: 109.0, 7.5: 109.0, 8.0: 117.0, 8.5: 117.0, 9.0: 125.0, 9.5: 125.0,
    10.0: 133.0, 10.5: 133.0, 11.0: 142.0, 11.5: 142.0, 12.0: 151.0,
    13.0: 151.0, 14.0: 160.0, 15.0: 169.0, 16.0: 178.0, 18.0: 191.0, 20.0: 205.0,
}


def standard_length(d: float) -> float:
    """Стандартная полная длина сверла Ø ``d`` мм по DIN 338.

    Берём запись ближайшего стандартного диаметра, не меньшего ``d`` (чем больше диаметр —
    тем длиннее сверло). Для ``d`` крупнее таблицы — самую длинную запись; для ``d <= 0`` —
    самую короткую (защитный фолбэк).
    """
    if d <= 0:
        return min(DIN338_LENGTHS.values())
    for size in sorted(DIN338_LENGTHS):
        if d <= size + 1e-9:
            return DIN338_LENGTHS[size]
    return DIN338_LENGTHS[max(DIN338_LENGTHS)]
