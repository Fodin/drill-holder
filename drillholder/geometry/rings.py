"""Декоративные колечки-инкрустации вокруг гнёзд (отдельная деталь «Rings»).

Плоское кольцо, утопленное заподлицо в рабочую грань вокруг гнезда. Косметика: внутренний радиус
отсчитывается от самого посадочного отверстия (``bore`` + зазор) — кольцо облегает дырку, а не
внешний край ниш пружин. Наружу кольцо уходит на ``width``, в грань — на ``depth``. Сама форма
ориентируется по нормали грани так же, как гнёзда и подписи: верх кольца ложится на грань, тело
уходит внутрь на ``depth``.

Сборка (``assembly``) вырезает кольцо из корпуса и собирает кольца в отдельную деталь под печать
вторым пластиком; точную подгонку посадочного места к телу (исключая фаску устья, пропилы пружин и
соседние гнёзда) делает пересечением с корпусом, поэтому здесь форма строится без оглядки на них.
"""

from __future__ import annotations

import FreeCAD as App
import Part

from ..model import RingSpec, Socket

_Z = App.Vector(0, 0, 1)
_ORIGIN = App.Vector(0, 0, 0)


def make_ring(socket: Socket, spec: RingSpec, surface, bore: float) -> Part.Shape:
    """Вернуть кольцо вокруг гнезда (``Part.Shape``), ориентированное по рабочей грани.

    ``bore`` — радиус посадочного отверстия (от него отсчитывается зазор ``gap``), чтобы кольцо
    облегало дырку. Канонически кольцо строится по +Z в ``[0, depth]`` (устье на ``z=depth``), затем
    ставится на грань: верх заподлицо с гранью, тело внутрь на ``depth`` — как гнёзда/гравировка.
    Точную обрезку по фаске устья и пропилам делает сборка пересечением с корпусом.
    """
    r_in = bore + spec.gap
    r_out = r_in + spec.width
    ring = Part.makeCylinder(r_out, spec.depth).cut(Part.makeCylinder(r_in, spec.depth))
    # '-'/'*' дают новый вектор; Vector.sub/multiply мутировали бы общий surface.normal.
    base = surface.point(socket.x, socket.y) - surface.normal * spec.depth
    ring.Placement = App.Placement(base, surface.rotation)
    return ring
