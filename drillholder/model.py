"""Доменная модель генератора подставки — чистые Value Objects без зависимости от FreeCAD.

Здесь живут только данные и перечисления. Никакой геометрии и никаких импортов FreeCAD:
этот модуль импортируется и тестируется в обычном Python. Геометрический слой (``geometry/``)
принимает на вход готовый :class:`ResolvedPlan` — это контракт-граница между ядром и адаптером.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from enum import Enum
from typing import List, Tuple


class ShankType(str, Enum):
    """Тип хвостовика сверла."""

    ROUND = "round"  # круглый хвостовик: ``d`` — диаметр (задаёт круглое гнездо)
    HEX = "hex"      # шестигранный (как у бит): гнездо — стандартный hex по ``socket.hex_size``;
    #                  ``d`` для hex — диаметр сверла, влияет только на подпись


class LayoutMode(str, Enum):
    """Способ раскладки гнёзд на подставке."""

    AUTO = "auto"      # выбрать режим автоматически по набору свёрел
    ROW = "row"        # один ряд по возрастанию диаметра
    GRID = "grid"      # прямоугольная сетка
    ANGLED = "angled"  # наклонная верхняя грань (организайзер)


class RowAlign(str, Enum):
    """Линия, по которой выстраиваются гнёзда разного размера в ряду (row/angled).

    В ряду гнёзда отсортированы по размеру; эта настройка задаёт, что держать на одной прямой.
    Передний край — сторона ``front`` (−Y, ближняя к пользователю, как у подписей).
    """

    CENTER = "center"  # по центрам гнёзд: оси на одной линии, края «ступеньками» с обеих сторон
    FRONT = "front"    # по переднему краю: ближние кромки всех гнёзд на линии переднего края крупнейшего
    BACK = "back"      # по заднему краю: дальние кромки всех гнёзд на линии заднего края крупнейшего


class LabelPosition(str, Enum):
    """С какой стороны от отверстия располагается подпись."""

    FRONT = "front"
    BACK = "back"
    LEFT = "left"
    RIGHT = "right"


class LabelReference(str, Enum):
    """Откуда отсчитывается отступ подписи — от края отверстия или от его центра."""

    EDGE = "edge"      # от края отверстия: зазор offset до текста одинаков при любом размере гнезда
    CENTER = "center"  # от центра отверстия: текст на фиксированном расстоянии от центра — подписи
    #                    выстраиваются ровно даже в сетке с гнёздами разного размера


class LabelMode(str, Enum):
    """Как наносить подпись на грань корпуса."""

    ENGRAVE = "engrave"  # гравировка: текст вырезается вглубь грани (углубление)
    EMBOSS = "emboss"    # выдавливание: текст выступает над гранью (рельеф)


class LidMode(str, Enum):
    """Тип крышки-колпака над свёрлами."""

    NONE = "none"            # без крышки
    REMOVABLE = "removable"  # съёмный колпак (надевается сверху)
    HINGED = "hinged"        # откидной колпак на боковых осях корпуса


class FootMode(str, Enum):
    """Режим устойчивого основания (foot)."""

    NONE = "none"      # без основания
    MANUAL = "manual"  # ручные размеры (margin/thickness)
    AUTO = "auto"      # авто по центру тяжести и высоте свёрел


@dataclass(frozen=True)
class Drill:
    """Одно сверло, которое подставка должна удерживать."""

    d: float                       # диаметр сверла (мм). round: задаёт гнездо. hex: только подпись
    length: float                  # длина сверла (мм) — задаёт глубину гнезда
    shank: ShankType = ShankType.ROUND
    label: str = ""                # текст подписи; пусто → сформируется из диаметра


@dataclass(frozen=True)
class SocketSpec:
    """Параметры гнезда (общие для всех свёрел)."""

    clearance: float = 0.20        # радиальный зазор СВОБОДНОЙ посадки, мм (держат детенты, не трение)
    depth_ratio: float = 0.35      # глубина гнезда = доля от длины сверла
    min_depth: float = 8.0         # минимальная глубина гнезда, мм
    max_depth: float = 40.0        # максимальная глубина гнезда, мм
    hex_size: float = 6.35         # стандартный размер «под ключ» hex-хвостовика, мм (1/4")
    hex_clearance: float = 0.25    # зазор «под ключ» для шестигранника, мм
    hex_flat_up: bool = True       # True — плоскостью вверх, False — вершиной вверх


@dataclass(frozen=True)
class SpringSpec:
    """Листовые пружины-детенты в стенке гнезда, удерживающие хвостовик от ВЫПАДАНИЯ.

    Дают осевую фиксацию для обоих типов хвостовика: круглого (лепестки по окружности) и hex
    (детенты на гранях призмы — грани держат от проворота, детенты от выпадания). Модель «трубки»:
    в стенке гнезда двумя продольными пропилами выделяется полоска-балка (лепесток), закреплённая
    сверху и снизу. Посередине лепестка — утолщение (детент), выступающее внутрь к хвостовику;
    за лепестком зазор, чтобы он отгибался наружу. Вдоль гнезда располагается ``rows`` таких
    пружин — две дают опору в двух точках по оси (хвостовик не болтается), одна подлиннее экономит
    глубину.
    """

    # count/rows/leaf_width — ЖЕЛАЕМЫЙ МАКСИМУМ; layout.fit_springs ужимает их под бор и глубину
    # каждого гнезда (мелкое сверло — узкий язычок с тем же детентом, не урезает крупные гнёзда).
    enabled: bool = True           # пружины вкл/выкл
    count: int = 3                 # макс. лепестков по окружности (круг) / детентов на гранях (hex, ≤6)
    rows: int = 2                  # макс. пружин вдоль оси гнезда (1 или 2)
    leaf_width: float = 1.8        # макс. ширина лепестка по окружности (хорда), мм
    leaf_length: float = 5.0       # свободная длина лепестка вдоль оси (длина пропилов), мм
    leaf_thickness: float = 1.0    # радиальная толщина лепестка (стенки), мм
    slot_width: float = 0.6        # ширина бокового пропила, освобождающего лепесток, мм
    relief_gap: float = 0.6        # радиальный зазор за лепестком для отгиба наружу, мм
    bump_protrusion: float = 0.40  # выступ детента внутрь за стенку гнезда, мм
    bump_radius: float = 0.5       # радиус скругления детента (усилие вставки/печать), мм


@dataclass(frozen=True)
class LayoutSpec:
    """Параметры раскладки гнёзд."""

    mode: LayoutMode = LayoutMode.AUTO
    grid_cols: int = 0             # столбцов в сетке (0 = авто = ceil(sqrt(N)))
    serpentine: bool = False       # сетка «змейкой»: чётные ряды слева направо, нечётные — справа
    #                                налево, чтобы соседние по размеру свёрла шли непрерывно (grid)
    angle_deg: float = 20.0        # угол наклона верхней грани для angled, град
    pitch_extra: float = 4.0       # доп. зазор между гнёздами, мм
    edge_margin: float = 6.0       # отступ гнёзд от края корпуса, мм
    row_gap: float = 0.0           # доп. зазор между рядами (0 = как pitch)
    align: RowAlign = RowAlign.CENTER  # линия выравнивания гнёзд внутри ряда (center/front/back; и для grid)


@dataclass(frozen=True)
class BaseSpec:
    """Параметры корпуса подставки."""

    floor: float = 5.0             # толщина дна под гнёздами, мм
    min_height: float = 18.0       # минимальная высота корпуса, мм


@dataclass(frozen=True)
class LabelSpec:
    """Параметры подписей (гравировка или выдавливание)."""

    enabled: bool = True
    mode: LabelMode = LabelMode.ENGRAVE              # вырезать вглубь или выдавить рельефом
    position: LabelPosition = LabelPosition.FRONT    # с какой стороны от отверстия
    reference: LabelReference = LabelReference.EDGE  # отступ от края отверстия или от его центра
    size: float = 4.0              # высота шрифта, мм
    depth: float = 0.6             # глубина гравировки / высота выдавливания, мм
    font: str = ""                 # путь к TTF-шрифту; пусто → подписи пропускаются
    offset: float = 2.0            # зазор от точки привязки (край/центр) до ближней кромки текста, мм
    multicolor: bool = False       # подписи отдельной деталью «Labels» под многоцветную/мультиматериальную
    #                                печать (QIDI BOX и т.п.): в корпусе вырезается посадочное место,
    #                                текст печатается отдельным STL другим пластиком — и для гравировки, и
    #                                для рельефа. False → текст слит с корпусом (cut/fuse) одним цветом.


@dataclass(frozen=True)
class RingSpec:
    """Декоративные колечки-инкрустации вокруг гнёзд (отдельная деталь «Rings»).

    Чисто косметика: вокруг каждого гнезда в верхней грани заподлицо утоплено плоское кольцо.
    В корпусе вырезается посадочная канавка, само кольцо экспортируется отдельной деталью под
    печать вторым пластиком (тем же, что и подписи) — в слайсере оба садятся в свои выемки
    заподлицо. На посадку/удержание сверла не влияет.
    """

    enabled: bool = False
    width: float = 1.5             # радиальная ширина колечка, мм
    depth: float = 0.6             # глубина инкрустации (заподлицо в грань), мм
    gap: float = 0.0               # зазор от края отверстия до внутренней кромки колечка, мм


@dataclass(frozen=True)
class LidSpec:
    """Крышка-колпак над торчащими свёрлами (отдельная печатная деталь).

    Высота подбирается по максимальному выступу сверла над верхней гранью. Откидной вариант
    защёлкивается боковыми ушками на круглые оси (штыри) корпуса. Поддерживается только для
    прямых раскладок (не angled).
    """

    mode: LidMode = LidMode.NONE
    wall: float = 2.0            # толщина стенок колпака, мм
    clearance: float = 0.4      # зазор между внутренней стенкой колпака и корпусом, мм
    top_gap: float = 3.0        # зазор над самым высоким сверлом, мм
    skirt: float = 8.0          # на сколько юбка колпака заходит на бока корпуса, мм
    # Съёмный: удерживающий рёбра-защёлка по низу юбки.
    snap: bool = True
    snap_protrusion: float = 0.5  # выступ защёлки внутрь, мм
    # Откидной: боковые оси корпуса и ушки крышки.
    pin_diameter: float = 4.0     # диаметр оси (штыря), мм
    pin_length: float = 3.0       # выступ оси из бока корпуса, мм
    ear_thickness: float = 3.0    # материал-кольцо вокруг оси, мм (радиус скруглённого зада боковой стенки)
    hinge_clearance: float = 0.4  # зазор отверстия ушка относительно оси, мм


@dataclass(frozen=True)
class FootSpec:
    """Устойчивое основание (расширенная опорная площадка снизу корпуса)."""

    mode: FootMode = FootMode.NONE
    thickness: float = 3.0       # толщина плиты основания, мм
    margin: float = 8.0          # MANUAL: вынос за габарит корпуса с каждой стороны, мм
    min_margin: float = 4.0      # AUTO: минимальный вынос за габарит, мм
    tip_angle_deg: float = 15.0  # AUTO: угол опрокидывания, под который считаем устойчивость
    chamfer: float = 0.0         # фаска по нижней кромке основания (0 — без), мм


class DrillStyle(str, Enum):
    """Форма режущей части в 3D-модели сверла."""

    SPIRAL = "spiral"      # реалистичная спиральная режущая часть (канавки)
    CYLINDER = "cylinder"  # гладкий цилиндр без канавок (проще и быстрее)


@dataclass(frozen=True)
class DrillGeometrySpec:
    """Параметры опциональных 3D-моделей самих свёрел (отдельная деталь «Drills»).

    По умолчанию выключено. Если включить — для каждого сверла строится модель (хвостовик в гнезде,
    режущая часть — спиральная или гладкий цилиндр по ``style``, конический кончик) и все они
    собираются в одну отдельную деталь, не сливаемую с корпусом: её удобно скрыть/показать во
    FreeCAD, а при экспорте она пишется отдельным файлом (печатать металлические свёрла, разумеется,
    не нужно). Качество спирали (число слоёв) и тип хвостовика подбираются автоматически. Параметры
    ``flutes``/``helix_angle_deg``/``flute_depth_ratio`` действуют только при ``style=spiral``.
    """

    enabled: bool = False                  # генерировать ли деталь «Drills» (по умолчанию ВЫКЛ)
    style: DrillStyle = DrillStyle.SPIRAL  # форма режущей части: спираль или гладкий цилиндр
    flutes: int = 2                        # число спиральных канавок (2 — стандарт; только spiral)
    helix_angle_deg: float = 30.0          # угол винтовой линии от оси, град (только spiral)
    point_angle_deg: float = 118.0         # угол при вершине, град (118 — стандарт по металлу)
    flute_depth_ratio: float = 0.28        # глубина канавки как доля радиуса (вид спирали; только spiral)


@dataclass(frozen=True)
class OutputSpec:
    """Параметры экспорта."""

    directory: str = "."
    name: str = "drill_holder"
    formats: Tuple[str, ...] = ("FCStd", "STL", "STEP")
    # Параметры тесселяции для STL:
    linear_deflection: float = 0.1   # макс. линейное отклонение меша, мм
    angular_deflection: float = 0.5  # макс. угловое отклонение меша, рад


@dataclass(frozen=True)
class Specs:
    """Все спецификации, кроме списка свёрел — удобно передавать одним объектом."""

    socket: SocketSpec = field(default_factory=SocketSpec)
    springs: SpringSpec = field(default_factory=SpringSpec)
    layout: LayoutSpec = field(default_factory=LayoutSpec)
    base: BaseSpec = field(default_factory=BaseSpec)
    labels: LabelSpec = field(default_factory=LabelSpec)
    rings: RingSpec = field(default_factory=RingSpec)
    lid: LidSpec = field(default_factory=LidSpec)
    foot: FootSpec = field(default_factory=FootSpec)
    drill_geometry: DrillGeometrySpec = field(default_factory=DrillGeometrySpec)
    output: OutputSpec = field(default_factory=OutputSpec)


@dataclass(frozen=True)
class Socket:
    """Разрешённое гнездо: сверло + позиция на плоскости раскладки + вычисленная глубина."""

    drill: Drill
    x: float                       # координата центра гнезда, мм
    y: float
    depth: float                   # глубина гнезда, мм
    outer_radius: float            # внешний радиус кармана (с учётом лепестков), мм


@dataclass(frozen=True)
class ResolvedPlan:
    """Полностью разрешённый план — единственный вход геометрического слоя.

    Содержит конкретные гнёзда (позиция + глубина) и все спецификации. После его построения
    математика раскладки больше не нужна: ``geometry/`` просто переводит план в твёрдое тело.
    """

    sockets: List[Socket]
    specs: Specs
    resolved_mode: LayoutMode      # фактически выбранный режим (auto уже разрешён)
