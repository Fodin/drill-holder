def _pyval(v: Any) -> str:
    """Литерал Python для значения конфига (строки в двойных кавычках, bool как True/False)."""
    if isinstance(v, bool):
        return "True" if v else "False"
    if isinstance(v, str):
        return '"' + v.replace("\\", "\\\\").replace('"', '\\"') + '"'
    if isinstance(v, (list, tuple)):
        return "[" + ", ".join(_pyval(x) for x in v) + "]"
    return repr(v)


def _fmt_drill(d: dict) -> str:
    """Одно сверло одной строкой: d, length, shank и (если задан) label."""
    parts = [
        f'"d": {_pyval(d.get("d"))}',
        f'"length": {_pyval(d.get("length", "auto"))}',
        f'"shank": {_pyval(str(d.get("shank", "round")))}',
    ]
    label = str(d.get("label", "")).strip()
    if label:
        parts.append(f'"label": {_pyval(label)}')
    return "{" + ", ".join(parts) + "}"


def render_config(cfg: dict) -> str:
    """Сериализовать конфиг в самодокументированный исходник ``holder_config.py``.

    В отличие от ``pprint``, сохраняет комментарии-документацию и ровные 4-пробельные отступы:
    перезаписанный из GUI файл остаётся читаемым источником истины. Недостающие ключи берутся
    из :data:`DEFAULTS`. Результат снова парсится :func:`load_raw_config` (полный round-trip).
    """
    def g(section: str, key: str) -> Any:
        return cfg.get(section, {}).get(key, DEFAULTS[section][key])

    out: List[str] = [
        '"""Конфигурация подставки для свёрел — единственный источник истины (правится вручную).',
        "",
        "Обычный Python: меняйте значения, комментарии поясняют каждый параметр. Любой пропущенный",
        "ключ берётся из DEFAULTS (drillholder/config/defaults.py). Сборка:",
        "    ./drill-holder            # с диалогом правки",
        "    ./drill-holder --no-gui   # сразу по этому файлу",
        '"""',
        "",
        "CONFIG = {",
    ]

    for line in _DRILLS_INTRO:
        out.append("    # " + line)
    out.append('    "drills": [')
    for drill in cfg.get("drills", DEFAULTS["drills"]):
        out.append("        " + _fmt_drill(drill) + ",")
    out.append("    ],")
    out.append(
        '    "default_shank": %s,  # тип хвостовика по умолчанию: "round" / "hex"'
        % _pyval(str(cfg.get("default_shank", DEFAULTS["default_shank"])))
    )

    for name, title, intro, fields in _CONFIG_SECTIONS:
        out.append("")
        out.append(f"    # ── {title} " + "─" * max(3, 60 - len(title)))
        for line in intro:
            out.append("    # " + line)
        rendered = [(f'"{key}": {_pyval(g(name, key))},', comment) for key, comment in fields]
        width = max(len(code) for code, _ in rendered)
        out.append(f'    "{name}": {{')
        for code, comment in rendered:
            out.append(f"        {code.ljust(width)}  # {comment}")
        out.append("    },")

    out.append("}")
    return "\n".join(out) + "\n"
