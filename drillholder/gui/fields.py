class Kind(Enum):
    """Тип элемента управления для параметра."""

    FLOAT = "float"      # QDoubleSpinBox
    INT = "int"          # QSpinBox
    BOOL = "bool"        # QCheckBox
    CHOICE = "choice"    # QComboBox по Enum
    TEXT = "text"        # QLineEdit
    FONT = "font"        # QLineEdit + кнопки «Система…»/«Файл…»
    FORMATS = "formats"  # группа чекбоксов FCStd/STL/STEP


@dataclass(frozen=True)
class FieldSpec:
    """Декларация одного параметра конфига для формы.

    ``section`` — ключ секции в конфиге (``"socket"``, ``"layout"``, …) или ``""`` для
    параметра верхнего уровня (``default_shank``). ``key`` — ключ внутри секции. ``choices``
    задан только для :attr:`Kind.CHOICE` (Enum из :mod:`drillholder.model`).
    """

    section: str
    key: str
    label: str
    kind: Kind
    tooltip: str = ""
    choices: Optional[Type[Enum]] = None
    minimum: float = 0.0
    maximum: float = 100000.0
    decimals: int = 2
    step: float = 0.1


