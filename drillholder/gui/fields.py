"""Декларативное описание полей формы — единый источник правды о виджетах конфига.

Каждый скалярный параметр конфига описан одним :class:`FieldSpec` (секция, ключ, тип виджета,
диапазон, подпись-tooltip). Список собирается :func:`build_field_specs`, который **итерирует**
``render._CONFIG_SECTIONS`` (оттуда берутся ключи и готовые русские комментарии-документация)
и накладывает таблицу типов/диапазонов/Enum из :data:`_FIELD_META`. Так форма автоматически
покрывает каждый параметр и не разъезжается с :data:`DEFAULTS`/``render``.

Модуль чистый: Qt здесь не импортируется (тестируется обычным ``python3``). Виджеты строит
:mod:`drillholder.gui.widgets` по этим спецификациям.
"""

