def choice_display(member: Enum) -> str:
    """Русская подпись значения для комбобокса; фолбэк — само Enum-значение.

    Ищем по классу Enum, затем по члену — чтобы str-Enum разных типов с одинаковым значением
    (например ``center`` у RowAlign и LabelReference) не путались.
    """
    return CHOICE_LABELS.get(type(member), {}).get(member, member.value)


