_DEFAULT_SHANK_TOOLTIP = "тип хвостовика по умолчанию для свёрел без явного: круглый / шестигранный"


# Ширина подсказки в символах. Qt-tooltip из плоского текста переносит строки только по '\n',
# поэтому длинные подсказки рвём по словам сами, иначе они вылезают за окно приложения.
TOOLTIP_WIDTH = 58


def wrap_tooltip(text: str, width: int = TOOLTIP_WIDTH) -> str:
    """Перенести подсказку по словам до ``width`` символов в строке (сохраняя явные ``\\n``).

    Чистая функция (без Qt). Применяется слоем виджетов ко всем подсказкам — базовым и к причинам
    дизейбла/невалидности — чтобы ни одна не растягивалась за пределы окна.
    """
    if not text:
        return ""
    return "\n".join(
        textwrap.fill(para, width=width) if para else "" for para in text.split("\n")
    )


def _meta_for(section: str, key: str) -> Tuple[Kind, dict]:
    return _FIELD_META.get((section, key), (Kind.FLOAT, {}))


def _make_spec(section: str, key: str, tooltip: str) -> FieldSpec:
    kind, kwargs = _meta_for(section, key)
    label = _FIELD_LABELS.get((section, key), key)
    # Развёрнутая подсказка формы, если есть; иначе — компактный комментарий конфига.
    tooltip = _FIELD_TOOLTIPS.get((section, key), tooltip)
    return FieldSpec(section=section, key=key, label=label, kind=kind, tooltip=tooltip, **kwargs)


def build_field_specs() -> Tuple[FieldSpec, ...]:
    """Собрать спецификации всех скалярных полей конфига (кроме таблицы ``drills``).

    Порядок: ``default_shank`` (верхний уровень), затем секции в порядке
    ``render._CONFIG_SECTIONS`` с их полями. Ключи и tooltip'ы берутся из ``_CONFIG_SECTIONS``
    (единый источник документации), тип виджета — из :data:`_FIELD_META`.
    """
    specs = [_make_spec("", "default_shank", _DEFAULT_SHANK_TOOLTIP)]
    for section, _title, _intro, fields in _CONFIG_SECTIONS:
        for key, comment in fields:
            specs.append(_make_spec(section, key, comment))
    return tuple(specs)
