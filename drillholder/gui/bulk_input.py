"""Разбор «массового» ввода диаметров: строка → список значений для таблицы свёрел.

Чистый модуль без Qt (импортируется и тестируется обычным ``python3``). Формат — как удобно
вставить из заметок или прайс-листа: числа, разделённые запятыми, пробелами, переносами строк
или точкой с запятой (``"1, 1.5, 2"`` ⇒ ``[1.0, 1.5, 2.0]``). Десятичный разделитель — точка.

Дубликаты НЕ убираются (у одного диаметра может быть несколько свёрел). Непарсящиеся куски не
роняют разбор: возвращаются отдельным списком, чтобы вызывающая сторона спокойно о них сообщила.
"""

from __future__ import annotations

import re
from typing import List, Tuple

# Разделители: запятая, точка с запятой, любой пробельный символ (включая перенос строки).
_SEPARATORS = re.compile(r"[,;\s]+")


def parse_diameters(text: str) -> Tuple[List[float], List[str]]:
    """Разобрать строку диаметров. Вернуть ``(значения, нераспознанные_куски)``.

    Значения — в порядке появления, с сохранением дубликатов. Сортировку/вставку делает
    вызывающая сторона. Отбрасываются только неположительные и нечисловые токены — они
    попадают во второй список как есть (для предупреждения пользователю).
    """
    values: List[float] = []
    errors: List[str] = []
    for token in _SEPARATORS.split(text.strip()):
        if not token:
            continue
        try:
            value = float(token)
        except ValueError:
            errors.append(token)
            continue
        if value <= 0.0:
            errors.append(token)
            continue
        values.append(value)
    return values, errors
